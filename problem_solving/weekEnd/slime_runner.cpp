    PauseUi(int x, int y, int width, int height) : Object(width, height) {
        X = x;
        Y = y;
        Active = false;
        Width = width;
        Height = height;
        SetDefaultImage(0);
        ImagePaste();
    }

    void ResetPos() {
        X = DefaultX;
        Y = DefaultY;
        Active = false;
    }

    void PauseGame(Scene& s) {
        for (int i = 3; i > 0; i--) {
            gotoxy(0, 0);
            s.ClearScreenBuf();
            SetDefaultImage(i);
            ImagePaste();
            s.Draw();
            Sleep(1000);
        }
        setActive(false);
        SetDefaultImage(0);
        ImagePaste();
    }

    void setActive(bool b) {
        Active = b;
    }

    void SetDefaultImage(int number) {
        for (int i = 0; i < 18; i++) {
            for (int j = 0; j < 38; j++) {
                if (number == 3) {
                    DefaultImage[i][j] = ThreeImage[i][j];
                } else if (number == 2) {
                    DefaultImage[i][j] = TwoImage[i][j];
                } else if (number == 1) {
                    DefaultImage[i][j] = OneImage[i][j];
                } else if (number == 0) {
                    DefaultImage[i][j] = PauseImage[i][j];
                }
            }
        }
    }

    void ImagePaste() {
        for (int i = 0; i < 18; i++) {
            for (int j = 0; j < 38; j++) {
                if (DefaultImage[i][j] == "■"){
                    DefaultImage[i][j] = Gray"■";
                } else if (DefaultImage[i][j] == "▤") {
                    DefaultImage[i][j] = Black"▤" Reset;
                } else if (DefaultImage[i][j] == "□") {
                    DefaultImage[i][j] = White"□";
                } else if (DefaultImage[i][j] == "▣") {
                    DefaultImage[i][j] = Green"▣";
                } else if (DefaultImage[i][j] == "▧") {
                    DefaultImage[i][j] = Yellow"▧";
                } else if (DefaultImage[i][j] == "▦") {
                    DefaultImage[i][j] = Red"▦";
                }
                Image[i][j] = DefaultImage[i][j];
            }
        }
    }

    void Render(Scene& s) {
        for (int i = 0; i < Height; i++) {
            if ((Y + i) < 39) {
                for (int j = 0; j < Width; j++) {
                    if (0 <= (X + j) && (X + j) < 100) {
                        if (Image[i][j] != "  ") {
                            s.ScreenBuffer[Y + i][X + j] = Image[i][j];
                        }
                    }
                }
            }
        }
    }

